
/**
 * Math Method
Math.abs(number) : number
인수의 절댓값 반환한다.
출처: https://inpa.tistory.com/entry/JS-📚-Math-메소드-✏️-정리 [Inpa Dev 👨‍💻:티스토리]
 */
Math.abs(-1);       // 1
Math.abs('-1');     // 1
Math.abs('');       // 0
Math.abs([]);       // 0
Math.abs(null);     // 0
Math.abs(undefined);// NaN

//Math.round(Number):Number
//인수의 소수점 이하를 반올림한 정수를 반환한다.

Math.round(1.4);  // 1
Math.round(1.6);  // 2
Math.round(-1.4); // -1
Math.round(-1.6); // -2
Math.round(1);    // 1
Math.round();     // NaN

// Math.ceil(Number):Number
// 인수의 소수점 이하를 올림한 정수를 반환다 

Math.ceil(1.4);  // 2
Math.ceil(1.6);  // 2
Math.ceil(-1.4); // -1
Math.ceil(-1.6); // -1
Math.ceil(1);    // 1
Math.ceil();     // NaN


